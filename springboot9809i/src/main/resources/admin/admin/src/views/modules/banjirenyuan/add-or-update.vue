						this.ro.shouji = true;
					}
					if(this.$storage.get("role")!="管理员") {
						this.ro.shouji = true;
					}
				} else {
					this.$message.error(data.msg);
				}
			});
			
            this.banjirenshuOptions = "1".split(',')
			
