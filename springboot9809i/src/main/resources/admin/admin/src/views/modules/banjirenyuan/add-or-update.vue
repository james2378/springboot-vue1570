				shenqingriqi: [
				],
				shenqingneirong: [
				],
				xuehao: [
					{ required: true, message: '学号不能为空', trigger: 'blur' },
				],
				shouji: [
				],
				crossuserid: [
				],
				crossrefid: [
				],
				sfsh: [
				],
				shhf: [
				],
			}
		};
	},
	props: ["parent"],
	computed: {



	},
    components: {
    },
	created() {
		this.ruleForm.shenqingriqi = this.getCurDate()
	},
	methods: {
		
		// 下载
		download(file){
			window.open(`${file}`)
		},
		// 初始化
		init(id,type) {
			if (id) {
				this.id = id;
				this.type = type;
			}
			if(this.type=='info'||this.type=='else'){
				this.info(id);
			}else if(this.type=='logistics'){
				this.logistics=false;
				this.info(id);
			}else if(this.type=='cross'){
				var obj = this.$storage.getObj('crossObj');
				for (var o in obj){
						if(o=='banjimingcheng'){
							this.ruleForm.banjimingcheng = obj[o];
							this.ro.banjimingcheng = true;
							continue;
						}
						if(o=='jiaoshigonghao'){
							this.ruleForm.jiaoshigonghao = obj[o];
							this.ro.jiaoshigonghao = true;
							continue;
						}
						if(o=='banjirenshu'){
							this.ruleForm.banjirenshu = obj[o];
							this.ro.banjirenshu = true;
							continue;
						}
						if(o=='shenqingriqi'){
							this.ruleForm.shenqingriqi = obj[o];
							this.ro.shenqingriqi = true;
							continue;
						}
						if(o=='shenqingneirong'){
							this.ruleForm.shenqingneirong = obj[o];
							this.ro.shenqingneirong = true;
							continue;
						}
						if(o=='xuehao'){
							this.ruleForm.xuehao = obj[o];
							this.ro.xuehao = true;
							continue;
						}
						if(o=='shouji'){
							this.ruleForm.shouji = obj[o];
							this.ro.shouji = true;
							continue;
						}
						if(o=='crossuserid'){
							this.ruleForm.crossuserid = obj[o];
							this.ro.crossuserid = true;
							continue;
						}
						if(o=='crossrefid'){
							this.ruleForm.crossrefid = obj[o];
							this.ro.crossrefid = true;
							continue;
						}
				}
				


				this.ruleForm.banjirenshu = 0
				this.ro.banjirenshu = false;









			}
			
			
			// 获取用户信息
			this.$http({
				url: `${this.$storage.get('sessionTable')}/session`,
				method: "get"
			}).then(({ data }) => {
				if (data && data.code === 0) {
					
					var json = data.data;
					if(this.$storage.get("role")!="管理员") {
						this.ro.banjimingcheng = true;
					}
					if(this.$storage.get("role")!="管理员") {
						this.ro.jiaoshigonghao = true;
					}
					if(this.$storage.get("role")!="管理员") {
						this.ro.shenqingriqi = true;
					}
